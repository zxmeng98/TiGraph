                    # if dist.is_initialized():
                    #     # Define bucket size (in elements)
                    #     bucket_size = 15 * 1024 * 1024  # 25MB per bucket
                        
                    #     # Collect all gradients
                    #     param_list = []
                    #     for param in model.parameters():
                    #         if not param.requires_grad:
                    #             continue
                            
